// Enhanced function to filter addresses based on input
export function filterAddresses(input: string): AddressData[] {
  if (!input || input.trim().length < 2) return []

  const normalizedInput = input.toLowerCase().trim()
  const words = normalizedInput.split(/\s+/)

  return addressData
    .filter((address) => {
      const streetLower = address.street.toLowerCase()
      const cityLower = address.city.toLowerCase()
      const stateLower = address.state.toLowerCase()
      const zipLower = address.zip.toLowerCase()
      const fullAddressLower = address.fullAddress.toLowerCase()

      // Check if any word in the input matches parts of the address
      const wordMatches = words.some(
        (word) =>
          streetLower.includes(word) ||
          cityLower.includes(word) ||
          stateLower.includes(word) ||
          zipLower.includes(word),
      )

      return wordMatches || fullAddressLower.includes(normalizedInput)
    })
    .sort((a, b) => {
      // First prioritize exact matches
      const aExactMatch = a.fullAddress.toLowerCase() === normalizedInput
      const bExactMatch = b.fullAddress.toLowerCase() === normalizedInput

      if (aExactMatch && !bExactMatch) return -1
      if (!aExactMatch && bExactMatch) return 1

      // Then prioritize matches that start with the input
      const aStartsWithStreet = a.street.toLowerCase().startsWith(normalizedInput)
      const bStartsWithStreet = b.street.toLowerCase().startsWith(normalizedInput)

      if (aStartsWithStreet && !bStartsWithStreet) return -1
      if (!aStartsWithStreet && bStartsWithStreet) return 1

      // Then prioritize by city
      const aInCity = a.city.toLowerCase().includes(normalizedInput)
      const bInCity = b.city.toLowerCase().includes(normalizedInput)

      if (aInCity && !bInCity) return -1
      if (!aInCity && bInCity) return 1

      // Then sort by address length (shorter addresses first)
      return a.fullAddress.length - b.fullAddress.length
    })
    .slice(0, 6) // Limit to 6 results for better UX
}

// Function to get mock coordinates for an address
export function getAddressCoordinates(address: string): { latitude: number; longitude: number } | null {
  const foundAddress = addressData.find((a) => a.fullAddress === address)

  if (foundAddress) {
    return {
      latitude: foundAddress.latitude,
      longitude: foundAddress.longitude,
    }
  }

  // If not found, return null or generate random coordinates
  return {
    latitude: 37.7749 + (Math.random() * 0.1 - 0.05),
    longitude: -122.4194 + (Math.random() * 0.1 - 0.05),
  }
}
